package java_Learning;

public class TypesofOperatorsday10 {

	public static void main(String[] args) {
		UnaryOperators();
		

	}

	private static void UnaryOperators() {
		int x = 5;
		//++ --> Increment Operator
		//Pre-Increment-->++x
		//Post-Increment-->x++
		System.out.println(++x);
		System.out.println(x++);
		System.out.println(x);
		System.out.println(--x);
		System.out.println(x);
		System.out.println(x--);
		
		//++ --> Decrement Operator
				//Pre-Decrement-->--x
				//Post-Decrement-->x--
		
	}


}
