package oops_Concepts;

public class LocalVaraible {

	public static void main(String[] args) {
		
		
		    int age = 25;                    // integer type
	        double salary = 50000.75;        // double type
	        char grade = 'A';                // character type
	        boolean isEmployee = true;       // boolean type
	        String name = "Lokesh";          // string type
	        float height = 5.9f;             // float type
	        long phoneNumber = 9876543210L;  // long type
	        byte rating = 5;                 // byte type
	        short year = 2025;               // short type
	        
	        System.out.println("Name: " + name);
	        System.out.println("Age: " + age);
	        System.out.println("Salary: " + salary);
	        System.out.println("Grade: " + grade);
	        System.out.println("Is Employee: " + isEmployee);
	        System.out.println("Height: " + height);
	        System.out.println("Phone Number: " + phoneNumber);
	        System.out.println("Rating: " + rating);
	        System.out.println("Year: " + year);

	}

}
